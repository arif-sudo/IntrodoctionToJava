package Eolymp.JavaOOP2;

import java.util.Scanner;

public class Main520 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        while (scan.hasNextLine()){
            System.out.println(scan.nextLine());
            scan.next();
            System.out.println(scan.nextLine());
            scan.next();
        }

    }
}
